from __future__ import annotations

from pathlib import Path
import uuid

from rdkit import Chem
from rdkit.Geometry import Point3D

import pytest

from src.quantum.mopac_adapter import MopacError, MopacOptimizer


TEST_TEMP_DIR = Path(__file__).resolve().parent / ".tmp"


def _molecule_with_conformer(smiles: str) -> Chem.Mol:
    base_molecule = Chem.MolFromSmiles(smiles)
    assert base_molecule is not None
    molecule = Chem.AddHs(base_molecule)
    conformer = Chem.Conformer(molecule.GetNumAtoms())
    for atom_index in range(molecule.GetNumAtoms()):
        conformer.SetAtomPosition(atom_index, Point3D(float(atom_index), 0.0, 0.0))
    molecule.AddConformer(conformer)
    return molecule


def test_build_keywords_includes_charge_and_eps() -> None:
    optimizer = MopacOptimizer({"method": "PM7", "use_mmok": True, "use_eps": True, "eps": 78.39})

    assert optimizer._build_keywords(1) == "PM7 MMOK OPT XYZ CHARGE=1 EPS=78.39"


